#include <stdio.h>
/*
 * Given this character array:
 * char array_ch[5] = {‘A’, ‘B’, ‘C’, ‘D’, ‘E’};
 * Write a program to display each element of the array on the screen
 */
int main()
{
char array_ch[5] = {'A', 'B', 'C', 'D', 'E'};
printf("array_ch[0] = %c\n", array_ch[0]);
printf("array_ch[1] = %c\n", array_ch[1]);
printf("array_ch[2] = %c\n", array_ch[2]);
printf("array_ch[3] = %c\n", array_ch[3]);
printf("array_ch[4] = %c\n", array_ch[4]);
return 0;
}
